
public class SwitchCase {

	public static void main(String[] args) {

		int zahl = 1;

		switch (zahl) {

		case 1: {
			System.out.println("1");
			break;
		}
		case 2: {
			System.out.println("2");
			break;
		}
		case 3: {
			System.out.println("3");
			break;
		}
		case 4: {
			System.out.println("4");
			break;
		}
		case 42: {
			System.out.println("42");
			break;
		}
		default: {
			System.out.println("Default");
			break;
		}

		
		}
	}
}
